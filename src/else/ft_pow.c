#include "else.h"

int		ft_pow(int x, int y)
{
	int a;

	a = x;
	if (y == 0)
		return (1);
	if (x == 1 || x == 0 || y == 1)
		return (x);
	while (y > 1)
	{
		x = x * a;
		y--;
	}
	return (x);
}
